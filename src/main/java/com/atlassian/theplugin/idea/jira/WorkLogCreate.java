    public Date getStartDate() {
        Date d = endTime;
        long t = d.getTime()
					- (listener.getWeeks() * Timer.ONE_WEEK)
					- (listener.getDays() * Timer.ONE_DAY)
					- (listener.getHours() * Timer.ONE_HOUR)
					- (listener.getMinutes() * Timer.ONE_MINUTE);
        d.setTime(t);
        return d;
    }

